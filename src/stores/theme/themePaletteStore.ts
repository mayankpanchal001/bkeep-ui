interface ThemePaletteState {
    selectedPaletteId: string;
    setPalette: (paletteId: string) => void;
    getPalette: () => ThemePalette | undefined;
}

export const useThemePaletteStore = create<ThemePaletteState>()(
    persist(
        (set, get) => ({
            selectedPaletteId: 'default',
            setPalette: (paletteId: string) =>
                set({ selectedPaletteId: paletteId }),
            getPalette: () => {
                const paletteId = get().selectedPaletteId;
                return THEME_PALETTES.find((p) => p.id === paletteId);
            },
        }),
        {
            name: 'bkeep-theme-palette-storage',
        }
    )
);
